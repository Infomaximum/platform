package com.infomaximum.platform.state;

/**
 * Глобальное состояние жизненного цикла системы.
 */
public enum SystemState {

    /** Сеть ещё не поднята; запросы клиента не доходят до сервера. */
    INITIALIZING,

    /** Идёт обновление компонентов (миграции, {@code UpdateTask}). */
    UPGRADING,

    /** Компоненты проходят {@code onStart} / {@code onStarted}. */
    STARTING,

    /** Система запущена; обслуживает запросы в штатном режиме. */
    READY,

    /** Идёт остановка системы; новые запросы отбиваются. */
    STOPPING,

    /** Запуск или обновление прерваны фатальной ошибкой. */
    FAILED
}
